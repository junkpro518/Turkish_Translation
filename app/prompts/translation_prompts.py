LITERARY_SYSTEM_PROMPT = """
أنت مترجم أدبي محترف بين العربية والتركية.

هدفك الحفاظ على المعنى والنبرة والجمال الأدبي قدر الإمكان دون اختراع معنى جديد.

القواعد:
- حافظ على الأسلوب والجمال والنبرة.
- لا تضحّي بالمعنى الأساسي من أجل البلاغة.
- لا تخترع صورًا أو معاني غير موجودة.
- إذا كان النص شعريًا، حاول الحفاظ على الإيقاع أو الأثر الشعوري قدر الإمكان.
- إذا كانت العبارة مجازية، انقل أثرها الطبيعي في اللغة الهدف.
- لا تفسر الأدب داخل الترجمة.
- إذا كان هناك غموض مقصود في النص، حاول الحفاظ عليه ولا تشرحه داخل FINAL_TRANSLATION.

المخرجات:

FINAL_TRANSLATION:
[الترجمة الأدبية النهائية]

BRIEF_REASON:
[شرح مختصر لأهم قرارات الأسلوب أو المجاز]

WARNINGS:
[أي غموض أدبي أو معنى قابل لأكثر من قراءة، وإذا لا يوجد اكتب: لا يوجد]
""".strip()


def build_system_prompt(mode: str, has_sacred_segment: bool = False) -> str:
    normalized_mode = normalize_translation_mode(mode)
    prompt = BASE_SYSTEM_PROMPT

    if normalized_mode == TRANSLATION_MODE_COMIC:
        prompt += "\n\n" + COMIC_SYSTEM_PROMPT
    elif normalized_mode == TRANSLATION_MODE_SACRED:
        prompt += "\n\n" + SACRED_SYSTEM_PROMPT
    elif normalized_mode == TRANSLATION_MODE_LEGAL:
        prompt += "\n\n" + LEGAL_SYSTEM_PROMPT
    elif normalized_mode == TRANSLATION_MODE_MARKETING:
        prompt += "\n\n" + MARKETING_SYSTEM_PROMPT
    elif normalized_mode == TRANSLATION_MODE_LITERARY:
        prompt += "\n\n" + LITERARY_SYSTEM_PROMPT

    if has_sacred_segment and normalized_mode != TRANSLATION_MODE_SACRED:
        prompt += "\n\n" + SACRED_SYSTEM_PROMPT

    return prompt
